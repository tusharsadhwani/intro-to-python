# Operators:
# how values in python interact with each other


x, y = 2, 3  # Multiple assignments in 1 line

print('x =', x)
print('y =', y)

# Arithemetic
print(x + y)
print(x ** y)
print(x + 0.5)
print(17 % 2)


# Comparative operators
print(x < y)
print(x == y)

# Logical operators
print('Are X and Y different?')
print(x > y or x < y)
